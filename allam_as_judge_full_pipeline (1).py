def judge_claim(claim):
    """Few-shot + CoT (لكن ننتبه أن المودل قد يكتب تفكير)"""

    prompt = f"""قم بتقييم صحة العبارات التالية مع التفكير خطوة بخطوة:

مثال 1:
العبارة: "الرياض هي عاصمة المملكة العربية السعودية"
التفكير: الرياض معروفة كعاصمة المملكة العربية السعودية منذ توحيدها
الحكم النهائي: العبارة صحيحة

مثال 2:
العبارة: "القاهرة هي عاصمة المملكة العربية السعودية"
التفكير: القاهرة هي عاصمة مصر وليست السعودية. عاصمة السعودية هي الرياض
الحكم النهائي: العبارة خاطئة

الآن، قيّم هذه العبارة:
العبارة: "{claim}"
التفكير:
الحكم النهائي:"""

    inputs = tokenizer(prompt, return_tensors="pt", truncation=True, max_length=512)
    inputs = {k: v.to(model.device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=30,   # 10 غالبًا قليل عشان يوصل "الحكم النهائي"
            do_sample=False,
            temperature=0.0,
            pad_token_id=tokenizer.eos_token_id,
            eos_token_id=tokenizer.eos_token_id
        )

    return tokenizer.decode(outputs[0], skip_special_tokens=True)

