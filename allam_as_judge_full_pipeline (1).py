def clean_claim(claim_text):
    """استخراج النص فقط من العبارة"""
    parts = str(claim_text).split()
    # نأخذ الجزء من النص بعد المعلومات التقنية
    if len(parts) > 4:
        return ' '.join(parts[4:])
    return claim_text

